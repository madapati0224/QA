package Operators;

public class Operator_Arithematic {
public static void main(String[] args) {
		
		//Arithematic operators (+,-,*,/,%)
		
		int a = 50;
		int b = 30;
	//	b=100; We can update the value of any variable when required without providing data type.
		
	System.out.println("sum of a and b is:"+(a+b)); //sum of a and b is:80 (a,b operands ,+ operator)
	System.out.println("sub of a and b is:"+(a-b)); //sub of a and b is:20
	System.out.println("mul of a and b is:"+(a*b)); //mul of a and b is:1500
	System.out.println("Div of a and b is:"+(a/b)); //Div of a and b is:1 (Displays quotient value as o/p)
	System.out.println("modulo of a and b is:"+(a%b)); //modulo of a and b is:20(Display remainder value as o/p)
	

}}
