package Operators;

public class Relational {
	
	 //Relational /Comparsion operators (>,<,>=,<=,!=,==)	

	public static void main(String[] args) {
		int a = 50;
		int b = 30;
		System.out.println(a>b); //true
		System.out.println(a<b); //false
		System.out.println(a>=b); //true
		System.out.println(a<=b); //false
		System.out.println(a!=b); //true
		System.out.println(a==b); //false

		}

	


	

}
