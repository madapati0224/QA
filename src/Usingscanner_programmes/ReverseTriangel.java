package Usingscanner_programmes;

import java.util.Scanner;

public class ReverseTriangel {

	public static void main(String[] args) {
		
		Scanner sc=new Scanner(System.in);
		System.out.println("Enter no.of rows to be printed:");
		int rows=sc.nextInt();
		
		for(int i=1;i<=rows;i++)//loop statements
		{
			
		 for(int j=rows;j>=i;j--)//*loop
		  {
			System.out.print("*");
		   }
		 
		  {
		   System.out.println();

	}

}
}}