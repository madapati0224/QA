package task;

public class Reverseof_Array {

	public static void main(String[] args) {
		// Print array values in reverse.

     int a[]={1,2,3,4,5};
   
System.out.println("order of number before reverse:");
     
     for(int i=0;i<a.length;i++)
     {
    // System.out.println(a[i]); 
       System.out.print(a[i]+" ");
	}   

     System.out.println();
  
 System.out.println("order of number after reverse:");
 //Reverse the array
 
   for(int i=a.length-1;i>=0;i--) 
   {
   // System.out.println(a[i]);  
	  System.out.print(a[i]+" ");
   }
 }}    
	
 





	


